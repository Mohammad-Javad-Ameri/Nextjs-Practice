import { createTheme } from "@mui/material/styles";

// Creating a light theme and Setting the mode of the palette to "light"
export const lightTheme = createTheme({
  palette: {
    mode: "light",
  },
});

// Creating a dark theme and Setting the mode of the palette to "dark"
export const darkTheme = createTheme({
  palette: {
    mode: "dark",
  },
});
